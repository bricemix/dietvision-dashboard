  # Surligne un mot-clé dans un texte (pour les logs)
  def highlight_keywords(text, keyword)
    return h(text) if keyword.blank?
    escaped_text = h(text)
    pattern = Regexp.escape(CGI.escapeHTML(keyword))
    highlighted = escaped_text.gsub(
      /#{pattern}/i,
      '<mark style="background:#c8ff0033;color:#c8ff00;border-radius:2px;padding:0 2px">\0</mark>'
    )
    highlighted.html_safe
  end

