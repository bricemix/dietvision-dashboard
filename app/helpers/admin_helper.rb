  # Exemple: stat_card("Utilisateurs", 2847, icon_users_svg, "text-blue-400")
  def stat_card(label, value, icon_svg, icon_color = "text-gray-400")
    content_tag :div, class: "bg-gray-900 rounded-xl border border-gray-800 p-5" do
      header = content_tag(:div, class: "flex items-center justify-between mb-3") do
        label_el = content_tag(:div, label, class: "text-xs text-gray-500 font-medium uppercase tracking-wide")
        icon_el  = content_tag(:span, icon_svg.html_safe, class: icon_color)
        label_el + icon_el
      end
      value_el = content_tag(:div, value.to_s, class: "text-2xl font-bold text-white")
      header + value_el
    end
  end

